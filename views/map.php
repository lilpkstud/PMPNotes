        <h3>Planning Phase</h3>
        <div class="container">
            <p><strong>Purpose:</strong> Define the course of action to successfully complete the project/phase.</p>
            <p><strong>Actions:</strong>  Establish the total scope, define/refine the objectives, and develop the course of action to achieve objectives.</p>
            <p><strong>When:</strong> Significant changes that occur during the lifecycle may require to revisit one or more processes or even an Initiating process.</p>
            <p>When the planning effort is completed, the approved Project Management Plan is the baseline
                <ul>
                    <li>The Monitoring & Controlling processes compare the project perforamnce to the baselines</li>
                </ul>
            </p>
        </div>
